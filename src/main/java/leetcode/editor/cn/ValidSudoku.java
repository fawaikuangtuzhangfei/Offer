class Solution {
    public boolean isValidSudoku(char[][] board) {
        Map<Integer, Integer>[] row = new HashMap[9];
        Map<Integer, Integer>[] line = new HashMap[9];
        Map<Integer, Integer>[] box = new HashMap[9];
        for (int i = 0; i < row.length; i++) {
            row[i] = new HashMap<>();
            line[i] = new HashMap<>();
            box[i] = new HashMap<>();
        }

        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                int temp = board[i][j];
                int boxId = i/3*3 + j/3;
                if('.' == board[i][j]){
                    continue;
                }
                if(row[i].containsKey(temp) || line[j].containsKey(temp) || box[boxId].containsKey(temp)){
                    return false;
                }else{
                    row[i].put(temp, 1);
                    line[j].put(temp, 1);
                    box[boxId].put(temp, 1);
                }
            }
        }
        return true;
    }
}
